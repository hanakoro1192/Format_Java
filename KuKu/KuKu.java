package KuKu;

public class KuKu {
    public static void main(String[] args){
        // System.out.println(args[0]);
        int kuku;

        for(int i = 0; i < 10; i++){
            for(int j = 0; j < 10; j++){
                kuku = i * j;
            }
            System.out.println("KuKuを表示");
        }
    }
}